package my.gov.ns.ptgns.doc_utils.viewmodel;

public class APIResponseVM {
    String status;
    String message;

    public APIResponseVM(String status, String message) {
        this.status = status;
        this.message = message;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }
}
